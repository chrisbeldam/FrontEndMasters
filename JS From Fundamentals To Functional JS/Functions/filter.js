const _ = {}

_.filter = function(arr, cb) {
    // create a new array
    const storage = [];
    // loop through array
    for(var i = 0; i<arr.length; i++){
        // check if callback returns true
        if (cb(arr[i], i, arr) === true){
            // push if so
            storage.push(arr[i]);
        }
    }
    // return array
    return arr;
}


